function unwrapKeys(k){
  if (Array.isArray(k)) return k;
  if (typeof k !== 'string') return [];
  let s = k;
  for (let i = 0; i < 3; i++) { try { const p = JSON.parse(s); s = p; if (Array.isArray(p)) return p; } catch { break; } }
  return Array.isArray(s) ? s : [];
}
function recoverEntry(e){
  if (!e) return e;
  return { id:e.id, name:e.name, keys:unwrapKeys(e.keys), content:e.content,
           constant:e.constant, priority:e.priority, insertion_order:e.insertion_order, scan_depth:e.scan_depth };
}
