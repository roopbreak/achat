try { db.exec('ALTER TABLE stories ADD COLUMN narration_style TEXT DEFAULT ""'); } catch {}
try { db.exec('ALTER TABLE stories ADD COLUMN narration_style_source TEXT DEFAULT "unset"'); } catch {}

const stmt = db.prepare(
  'UPDATE stories SET narration_style = ?, narration_style_source = ?, updated_at = unixepoch() WHERE name = ?'
);

const stories = db.prepare('SELECT name FROM stories ORDER BY name').all();
let updated = 0;
let skipped = 0;

for (const { name } of stories) {
  const style = styles[name];
  if (style === undefined) {
    // styles에 없는 스토리 — 현대물 기본 적용
    stmt.run(MODERN_BASE, 'auto', name);
    console.log(`  [기본] ${name} (현대물 기본)`);
    updated++;
  } else if (style === null) {
    console.log(`  [스킵] ${name}`);
    skipped++;
  } else {
    stmt.run(style, 'auto', name);
    console.log(`  [적용] ${name}`);
    updated++;
  }
}

db.close();
console.log(`\n완료: ${updated}개 적용, ${skipped}개 스킵 (총 ${stories.length}개)`);
